package com.example.myapplication2.Kotlin.lesson3;

public class Ajava {
    private final String a;
    private final int b;

    public Ajava(String a, int b) {
        this.a = a;
        this.b = b;
    }

    @Override
    public String toString() {
        return "Ajava{" +
                "a='" + a + '\'' +
                ", b=" + b +
                '}';
    }
}
